CREATE TABLE ITEM(
	ITEM_ID BIGSERIAL PRIMARY KEY,
	ITEM_NAME VARCHAR(16) ,
	ITEM_COST CHAR(4) ,
	ITEM_CALORIES CHAR(6),
	MEAL_ID BIGINT(20) 
	

);

CREATE TABLE MEAL(
	
	ID BIGINT(20) PRIMARY KEY,
	MEAL_NAME VARCHAR(64),
	MARKET_COST CHAR(4),
	OVERALL_COST CHAR(4),
	MEAL_CALORIES CHAR(6)
	
	

);
ALTER TABLE ITEM ADD FOREIGN KEY(MEAL_ID) REFERENCES MEAL(ID);